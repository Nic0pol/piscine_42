char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*original_dest;

	original_dest = dest;
	while (n > 0 && *src)
	{
		*dest++ = *src++;
		n--;
	}
	while (n > 0)
	{
		*dest++ = '\0';
		n--;
	}
	return (original_dest);
}
