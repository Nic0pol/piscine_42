char	*ft_strcapitalize(char *str)
{
	int	i;
	int	j;

	i = 0;
	while (str[i] != '\0')
	{
		j = 0;
		while (('a' <= str[i] && str[i] <= 'z')
			|| ('A' <= str[i] && str[i] <= 'Z')
			|| ('0' <= str[i] && str[i] <= '9'))
		{
			if ('a' <= str[i] && str[i] <= 'z' && j == 0)
				str[i] -= 32;
			if ('A' <= str[i] && str[i] <= 'Z' && j > 0)
				str[i] += 32;
			j++;
			i++;
		}
		i++;
	}
	return (str);
}
