#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] > 0 && str[i] < 48) || (str[i] > 57 && str[i] < 127))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
