int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
		{
			c = s1[i] - s2[i];
			return (c);
		}
		else
			i++;
	}
	return (0);
}
